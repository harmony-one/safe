    func data(hex str: String) -> Data {
        let value = stride(from: 0, to: str.count, by: 2).compactMap { offset in
            UInt8(str[str.index(str.startIndex, offsetBy: offset)..<str.index(str.startIndex, offsetBy: offset + 2)], radix: 16)
        }
        return Data(value)
    }

