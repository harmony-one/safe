import Foundation
import Solidity
import XCTest

class BoolEncodingTests: XCTestCase {

    func testEncodeTrue() {
        let value = Sol.Bool(storage: true)
        let data = value.encode()
        let expected = Data([
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // 16 bytes
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, // 32 bytes
        ])
        XCTAssertEqual(data, expected)
    }

    func testEncodeFalse() {
        let value = Sol.Bool(storage: false)
        let data = value.encode()
        let expected = Data([
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // 16 bytes
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // 32 bytes
        ])
        XCTAssertEqual(data, expected)
    }

    func testDecodeTrue() throws {
        let expected = Sol.Bool(storage: true)
        let data = Data([
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // 16 bytes
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, // 32 bytes
        ])
        var offset = 0
        let value = try Sol.Bool(from: data, offset: &offset)
        XCTAssertEqual(value, expected)
    }

    func testDecodeFalse() throws {
        let expected = Sol.Bool(storage: false)
        let data = Data([
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // 16 bytes
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // 32 bytes
        ])
        var offset = 0
        let value = try Sol.Bool(from: data, offset: &offset)
        XCTAssertEqual(value, expected)
    }

    func testDecodeInvalid() throws {
        let data = Data([
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // 16 bytes
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x02, // 32 bytes
        ])
        var offset = 0
        XCTAssertThrowsError(
            try Sol.Bool(from: data, offset: &offset)
        )
    }
}
