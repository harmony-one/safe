        let value = Sol.Bytes(storage: Data([0xef]))
        let data = value.encode()
        let expected = Data([
            // length: 1 byte (uint256)
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // 16 bytes
